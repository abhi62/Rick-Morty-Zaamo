const checkEmail = ({ email, setError }) => {
  const expression =
    /(?!.*\.{2})^([a-z\d!#$%&'*+\-\/=?^_`{|}~\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]+(\.[a-z\d!#$%&'*+\-\/=?^_`{|}~\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]+)*|"((([ \t]*\r\n)?[ \t]+)?([\x01-\x08\x0b\x0c\x0e-\x1f\x7f\x21\x23-\x5b\x5d-\x7e\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]|\\[\x01-\x09\x0b\x0c\x0d-\x7f\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]))*(([ \t]*\r\n)?[ \t]+)?")@(([a-z\d\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]|[a-z\d\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF][a-z\d\-._~\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]*[a-z\d\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])\.)+([a-z\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]|[a-z\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF][a-z\d\-._~\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]*[a-z\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])\.?$/i;

  const isValidEmail = expression.test(`${email}`);

  const accounts = localStorage.getItem("accounts");

  const isAccount =
    accounts && [...JSON.parse(accounts)]?.length >= 1
      ? [...JSON.parse(accounts)].find((item) => {
          return item.email === email;
        })
      : false;

  if (!isValidEmail) {
    setError((preValue) => {
      return {
        ...preValue,
        email: "Please enter a valid email id.",
      };
    });
    return { isEmailError: true };
  } else if (isAccount) {
    setError((preValue) => {
      return {
        ...preValue,
        email: "Email id has already been taken.",
      };
    });
    return { isEmailError: true };
  } else {
    setError((preValue) => {
      return {
        ...preValue,
        email: null,
      };
    });
    return { isEmailError: false };
  }
};

export { checkEmail };
